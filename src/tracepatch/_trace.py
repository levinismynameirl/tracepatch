def _format_elapsed(seconds: float) -> str:
    """Human-friendly elapsed time string."""
    ms = seconds * 1000
    if ms < 1:
        return f"{seconds * 1_000_000:.0f}us"
    if ms < 1000:
        return f"{ms:.2f}ms"
    return f"{seconds:.3f}s"


def render_tree(
    roots: list[TraceNode],
    *,
    style: str = "ascii",
    show_args: bool = True,
    show_return: bool = True,
    show_source: bool = False,
    show_self_time: bool = False,
) -> str:
    """Render a list of root ``TraceNode``s as a readable call tree.

    Parameters
    ----------
    roots:
        Top-level trace nodes to render.
    style:
        ``"ascii"`` for plain ASCII connectors, ``"unicode"`` for
        box-drawing characters, or ``"ansi"`` for box-drawing with
        dim-coloured connectors.
    show_args:
        Include function arguments in the output.
    show_return:
        Include return values / exceptions in the output.
    show_source:
        Include source file and line number for each call.
    show_self_time:
        Show self-time (total minus children) alongside the total elapsed time.

    Returns
    -------
    str
        The rendered multi-line tree string.
    """
    dim = "\033[2m"
    reset = "\033[0m"
    if style == "unicode":
        last_conn = "\u2514\u2500 "
        mid_conn = "\u251c\u2500 "
        cont = "\u2502  "
        space = "   "
    elif style == "ansi":
        last_conn = f"{dim}\u2514\u2500 {reset}"
        mid_conn = f"{dim}\u251c\u2500 {reset}"
        cont = f"{dim}\u2502  {reset}"
        space = "   "
    else:
        last_conn = "\u2514\u2500\u2500 "
        mid_conn = "\u251c\u2500\u2500 "
        cont = "\u2502   "
        space = "    "

    lines: list[str] = []

    def _walk(node: TraceNode, prefix: str, is_last: bool) -> None:
        connector = last_conn if is_last else mid_conn
        ret = ""
        if show_return:
            if node.exception is not None:
                ret = f" !! {node.exception}"
            elif node.return_value is not None:
                ret = f" -> {node.return_value}"

        timing = _format_elapsed(node.elapsed)
        if show_self_time and node.children:
            child_sum = sum(c.elapsed for c in node.children)
            self_sec = max(0.0, node.elapsed - child_sum)
            timing = f"{timing} / self: {_format_elapsed(self_sec)}"

        # Memory delta annotation (only when tracking enabled)
        mem_str = ""
        if node.memory_delta_kb is not None:
            delta = node.memory_delta_kb
            mem_str = f", {delta / 1024:+.1f} MB" if abs(delta) >= 1024 else f", {delta:+.1f} KB"

        args_part = f"({node.args})" if show_args else "()"
        label = f"{node.module}.{node.name}{args_part}{ret}  [{timing}{mem_str}]"
        if show_source and node.file and node.lineno is not None:
            label += f"  ({node.file}:{node.lineno})"
        lines.append(f"{prefix}{connector}{label}")

        extension = space if is_last else cont
        child_prefix = prefix + extension
        for i, child in enumerate(node.children):
            _walk(child, child_prefix, i == len(node.children) - 1)

    for i, root in enumerate(roots):
        _walk(root, "", i == len(roots) - 1)

    return "\n".join(lines)


