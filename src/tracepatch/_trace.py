# Trace summary: lightweight statistics computed from a TraceNode tree
# ---------------------------------------------------------------------------


@dataclass
class TraceSummary:
    """Pre-computed statistics for a trace.

    Attributes
    ----------
    call_count:
        Total number of calls in the tree.
    max_depth_reached:
        Maximum nesting depth observed.
    unique_functions:
        Set of ``module.name`` strings for every unique function seen.
    unique_modules:
        Set of module names.
    total_duration_ms:
        Sum of root-node elapsed times in milliseconds.
    slowest_call_ms:
        Elapsed time of the single slowest call in milliseconds.
    slowest_call_name:
        ``module.name`` of the slowest call, or ``""`` if no calls.
    most_called:
        Top-10 most frequently called functions as ``(name, count)`` pairs,
        sorted descending by count.
    """

    call_count: int = 0
    max_depth_reached: int = 0
    unique_functions: set[str] = field(default_factory=set)
    unique_modules: set[str] = field(default_factory=set)
    total_duration_ms: float = 0.0
    slowest_call_ms: float = 0.0
    slowest_call_name: str = ""
    most_called: list[tuple[str, int]] = field(default_factory=list)

    @property
    def unique_function_count(self) -> int:
        """Number of unique functions in the trace."""
        return len(self.unique_functions)

    @property
    def unique_module_count(self) -> int:
        """Number of unique modules in the trace."""
        return len(self.unique_modules)

    @classmethod
    def from_roots(cls, roots: list[TraceNode]) -> TraceSummary:
        """Compute summary statistics from a list of root ``TraceNode``s.

        Parameters
        ----------
        roots:
            Root-level trace nodes.

        Returns
        -------
        TraceSummary
        """
        summary = cls()
        call_counts: dict[str, int] = {}
        slowest_ms = 0.0
        slowest_name = ""
        max_depth = 0

        def _walk(node: TraceNode, depth: int) -> None:
            nonlocal slowest_ms, slowest_name, max_depth
            summary.call_count += 1
            if depth > max_depth:
                max_depth = depth
            fqn = f"{node.module}.{node.name}" if node.module else node.name
            summary.unique_functions.add(fqn)
            if node.module:
                summary.unique_modules.add(node.module)
            call_counts[fqn] = call_counts.get(fqn, 0) + 1
            elapsed_ms = node.elapsed * 1000
            if elapsed_ms > slowest_ms:
                slowest_ms = elapsed_ms
                slowest_name = fqn
            for child in node.children:
                _walk(child, depth + 1)

        for root in roots:
            summary.total_duration_ms += root.elapsed * 1000
            _walk(root, 0)

        summary.max_depth_reached = max_depth
        summary.slowest_call_ms = slowest_ms
        summary.slowest_call_name = slowest_name
        summary.most_called = sorted(
            call_counts.items(), key=lambda x: x[1], reverse=True
        )[:10]
        return summary


