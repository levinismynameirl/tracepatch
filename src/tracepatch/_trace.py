_CACHE_DIR_NAME = ".tracepatch"
_TRACES_SUBDIR = "traces"
_CACHE_GITIGNORE = "# Auto-generated by tracepatch. Do not edit.\n*\n"
_CACHE_README = """This directory is managed by tracepatch.

traces/     — JSON trace logs produced by trace() context managers.
state.json  — Setup/cleanup state for *tph run* / *tph clean*.

The traces/ subdirectory is git-ignored by default so trace logs
are never committed.  This top-level directory (and state.json)
*should* be committed.

You can read these logs with:

    from tracepatch import trace
    for entry in trace.logs():
        print(entry["file"], "-", entry["call_count"], "calls")

Or open any file directly — they are plain JSON.
"""


def _ensure_cache_dir(base: str | Path | None = None) -> Path:
    """Create the ``.tracepatch/traces/`` directory tree if needed.

    The layout is::

        .tracepatch/
        ├── README.md
        ├── state.json   (managed elsewhere)
        └── traces/
            └── .gitignore   (contains ``*``)

    Parameters
    ----------
    base:
        Parent directory for ``.tracepatch``, or the data directory
        itself.  Defaults to the current working directory.

    Returns
    -------
    Path
        Path to the ``traces/`` subdirectory where JSON logs are stored.
    """
    if base is None:
        parent = Path.cwd()
        data_dir = parent / _CACHE_DIR_NAME
    else:
        base_path = Path(base)
        # Accept both the parent and the data dir itself.
        data_dir = base_path if base_path.name == _CACHE_DIR_NAME else base_path / _CACHE_DIR_NAME

    data_dir.mkdir(exist_ok=True)

    # README at .tracepatch/README.md
    readme = data_dir / "README.md"
    if not readme.exists():
        readme.write_text(_CACHE_README, encoding="utf-8")

    # traces/ subdirectory
    traces_dir = data_dir / _TRACES_SUBDIR
    traces_dir.mkdir(exist_ok=True)

    # .gitignore inside traces/ only
    gitignore = traces_dir / ".gitignore"
    if not gitignore.exists():
        gitignore.write_text(_CACHE_GITIGNORE, encoding="utf-8")

    return traces_dir


# ---------------------------------------------------------------------------
# Trace node: one captured call
# ---------------------------------------------------------------------------


@dataclass
class TraceNode:
    """A single captured function call, forming a tree via children."""

    name: str
    module: str
    args: str
    return_value: str | None = None
    exception: str | None = None
    start: float = 0.0
    end: float = 0.0
    elapsed: float = 0.0
    children: list[TraceNode] = field(default_factory=list)
    depth: int = 0
    file: str | None = None
    lineno: int | None = None
    memory_delta_kb: float | None = None

    # ------------------------------------------------------------------
    # Serialisation helpers
    # ------------------------------------------------------------------

    def to_dict(self) -> dict[str, Any]:
        """Return a plain dict suitable for JSON serialisation."""
        d: dict[str, Any] = {
            "name": self.name,
            "module": self.module,
            "args": self.args,
            "return_value": self.return_value,
            "start": self.start,
            "end": self.end,
            "elapsed_ms": round(self.elapsed * 1000, 4),
        }
        if self.file is not None:
            d["file"] = self.file
        if self.lineno is not None:
            d["lineno"] = self.lineno
        if self.exception is not None:
            d["exception"] = self.exception
        if self.memory_delta_kb is not None:
            d["memory_delta_kb"] = round(self.memory_delta_kb, 2)
        if self.children:
            d["children"] = [c.to_dict() for c in self.children]
        return d


